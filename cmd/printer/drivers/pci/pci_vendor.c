/* we devide 65535 vendors to 256 segments, and record the start index of each segment 
   appeared in PciDevTable[].  Just For Faster Access.*/
static unsigned short start_of_vseg[255];
void PciDevTable_Mk_Fast_Access(void){
	int i;
	for(i = 0 ; i < sizeof(start_of_vseg)/sizeof(short); i++) 
												start_of_vseg[i] = 255;
	int done = -1;
	for(i = 0; i < sizeof(PciDevTable)/sizeof(struct pci_info_entry); i++){
		int fresh_seg = PciDevTable[i].VenId >> 8;
		if( fresh_seg == done) continue;

		done = fresh_seg;
		start_of_vseg[fresh_seg] = i;
	}
}

struct pci_info_entry * PciTable_Get(unsigned short vendor, unsigned short device){
	int seg = vendor >> 8;
	int start_idx = start_of_vseg[seg];
	if(start_idx == 0xff) return 0;		/*not registered */
	for(int i = start_idx; i < sizeof(PciDevTable) / sizeof(struct pci_info_entry); i++){
		if(PciDevTable[i].VenId == vendor && PciDevTable[i].DevId == device){
			return PciDevTable + i;	
		}
		if(PciDevTable[i].VenId >> 8 != seg)	return 0;	/* avoid extreamly traverse*/
	}
	return 0;
}

struct pci_vendor_entry *PciVendorTbl_Get(u16 vendor){
	for(int i = 0; i < PCI_VENTABLE_LEN; i++){
		if(PciVenTable[i].VenId == vendor) return PciVenTable + i;
	}
	return 0;
}


struct pci_class_entry *PciClassTbl_Get(u8 class, u8 sub, u8 prog){
	for(int i = 0; i < PCI_CLASSCODETABLE_LEN; i++){
		if(PciClassCodeTable[i].BaseClass == class &&
		   PciClassCodeTable[i].SubClass == sub &&
		   PciClassCodeTable[i].ProgIf == prog
		   )		return PciClassCodeTable + i;
	}
	return 0;
}


